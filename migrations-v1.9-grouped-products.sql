-- Pool Shed v1.9.2: controlled catalogue import batches and rollback audit
-- Additive only. Product rollback is performed by the application using stored before/after snapshots.
begin;

create table if not exists public.product_import_batches (
  id uuid primary key default gen_random_uuid(),
  file_name text,
  source text not null default 'csv',
  status text not null default 'completed' check (status in ('preview','completed','failed','undone')),
  uploaded_by uuid references auth.users(id) on delete set null,
  uploaded_at timestamptz not null default now(),
  total_rows integer not null default 0,
  created_rows integer not null default 0,
  updated_rows integer not null default 0,
  skipped_rows integer not null default 0,
  failed_rows integer not null default 0,
  error_report jsonb not null default '[]'::jsonb,
  undo_reason text,
  undone_by uuid references auth.users(id) on delete set null,
  undone_at timestamptz,
  metadata jsonb not null default '{}'::jsonb
);

create table if not exists public.product_import_changes (
  id bigint generated by default as identity primary key,
  batch_id uuid not null references public.product_import_batches(id) on delete restrict,
  variant_id uuid references public.products(id) on delete set null,
  sku text not null,
  change_type text not null check (change_type in ('created','updated','skipped','failed')),
  previous_value jsonb,
  new_value jsonb,
  created_at timestamptz not null default now()
);

create index if not exists product_import_batches_uploaded_at_idx on public.product_import_batches(uploaded_at desc);
create index if not exists product_import_changes_batch_idx on public.product_import_changes(batch_id,id);
create index if not exists product_import_changes_variant_idx on public.product_import_changes(variant_id);

alter table public.product_import_batches enable row level security;
alter table public.product_import_changes enable row level security;

-- Replace app_metadata role checks if your project uses a dedicated profiles/permissions table.
drop policy if exists "admin manage product import batches" on public.product_import_batches;
create policy "admin manage product import batches" on public.product_import_batches
for all to authenticated
using (coalesce(auth.jwt() -> 'app_metadata' ->> 'role','') in ('Admin','Manager'))
with check (coalesce(auth.jwt() -> 'app_metadata' ->> 'role','') in ('Admin','Manager'));

drop policy if exists "admin manage product import changes" on public.product_import_changes;
create policy "admin manage product import changes" on public.product_import_changes
for all to authenticated
using (coalesce(auth.jwt() -> 'app_metadata' ->> 'role','') in ('Admin','Manager'))
with check (coalesce(auth.jwt() -> 'app_metadata' ->> 'role','') in ('Admin','Manager'));

commit;

